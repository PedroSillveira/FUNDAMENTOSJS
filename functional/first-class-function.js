// 'first-calss-function' quando uma função é tratada como qualquer outro valor dentro do sistema


// function expression de soma
const soma = function(a, b){
    return a + b
}
// function expression de subtração
const subtracao = function(a, b){
    return a - b
}
// arrow function padrão
const vezes = (a, b) => a * b
// arrow function padrão
const divisao = (a, b) => a / b



console.log(soma(10,20))
console.log(subtracao(10,20))
console.log(vezes(10,20))
console.log(divisao(10,20))

